// ── Helpers ────────────────────────────────────────────────────────────────────

function _bhaFracToDecimal(s) {
  if (typeof s === 'number') return s;
  s = String(s).trim();
  const m = s.match(/^(\d+)\s+(\d+)\/(\d+)$/);
  if (m) return +m[1] + +m[2] / +m[3];
  const f = s.match(/^(\d+)\/(\d+)$/);
  if (f) return +f[1] / +f[2];
  return parseFloat(s) || 0;
}

