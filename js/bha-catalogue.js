function hwdpConnectionsByNom(type, nom) {
  const arr = type === 'spiral' ? HWDP_SPIRAL : HWDP_CONV;
  return arr.filter(r => r.nom === nom).flatMap(e => e.connections.map(c => c.conn));
}
function hwdpSpec(type, nom, conn) {
  const arr = type === 'spiral' ? HWDP_SPIRAL : HWDP_CONV;
  for (const entry of arr) {
    if (entry.nom !== nom) continue;
    const c = entry.connections.find(c => c.conn === conn);
    if (c) return {
      od_in: _bhaFracToDecimal(nom),
      id_in: _bhaFracToDecimal(entry.id),
      pf:    c.pf,
      conn:  c.conn,
    };
  }
  return null;
}
